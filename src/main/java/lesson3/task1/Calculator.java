package lesson3.task1;

public class Calculator {

    static int calculateSum(int a, int b) {
        return a + b;
    }

    static int subtract(int a, int b) {
        return a - b;
    }

    static int multiply(int a, int b) {
        return a * b;
    }

    static double divide(int a, int b) {
        return 1.0 * a / b;
    }
}
